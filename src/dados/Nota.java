package dados;

import dados.*;

public class Nota {
    private int id;
    private float valor;

    public Nota() {}

    public Nota(float valor) {
        this.valor = valor;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public float getValor() {
        return valor;
    }

    public void setValor(float valor) {
        this.valor = valor;
    }
}
